#include "mod/ctype.h"

#define collate(c) lc_collate[(int)(c)+1]
int lstrcmp (const char* s1, const char* s2) {
  const unsigned char* p = (const unsigned char*)s1;
  const unsigned char* q = (const unsigned char*)s2;
  short a, b;
  do {
    a = collate(*p++);
    b = collate(*q++);
  } while (a != 0 && a == b);
  return a - b;
}
#undef collate
#define collate(c) lc_collate[(int)(tolower(c))+1]
int lstrcasecmp (const char* s1, const char* s2) {
  const unsigned char* p = (const unsigned char*)s1;
  const unsigned char* q = (const unsigned char*)s2;
  short a, b;
  do {
    a = collate(*p++);
    b = collate(*q++);
  } while (a != 0 && a == b);
  return a - b;
}
int lstrncasecmp(const char* s1, const char* s2, size_t n) {
  const unsigned char* p = (const unsigned char*)s1;
  const unsigned char* q = (const unsigned char*)s2;
  short a = 0, b = 0;
  if (n >= 4) {
    size_t n4 = n >> 2;
    do {
      a = collate(*p++);
      b = collate(*q++);
      if (a == 0 || a != b)
        return a - b;
      a = collate(*p++);
      b = collate(*q++);
      if (a == 0 || a != b)
        return a - b;
      a = collate(*p++);
      b = collate(*q++);
      if (a == 0 || a != b)
        return a - b;
      a = collate(*p++);
      b = collate(*q++);
      if (a == 0 || a != b)
        return a - b;
    } while (--n4 > 0);
    n &= 3;
  }
  while (n > 0) {
      a = collate(*p++);
      b = collate(*q++);
    if (a == 0 || a != b)
      return a - b;
    n--;
  }
  return a - b;
}

